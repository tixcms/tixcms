<p>
    Блоки - это часть сайта, которая отображает кусок информации. Обычно блоки располагаются в левой или правой колонке. Примеры блоков: последние новости, случайное фото, навигация по сайту и другие.
</p>

<p>
    Блоки могут располагаться как самостоятельно на странице, так и сгруппированы в области. Группировка в области позволяет через панель управления добавлять новые блоки, убирать и менять порядок их вывода.
</p>